//Эта переменная определяет количество карточек, которые нужно показывать
//в десктопной версии при нажатии на кнопку "ещё".
// Значение равно 3 и при нажатии на кнопку "ещё" будет
//отображаться 3 карточки с фильмами.
const DESKTOP_CARDS_DISPLAY_LIMIT = 3
//Эта переменная определяет количество карточек, которые нужно показывать
//для планшетов при нажатии на кнопку "ещё".
// Значение равно 2 и при нажатии на кнопку "ещё" будет
//отображаться 2 карточки с фильмами.
const TABLET_CARDS_DISPLAY_LIMIT = 2
//Эта переменная определяет количество карточек, которые нужно показывать
//для мобильных устройств при нажатии на кнопку "ещё".
// Значение равно 2 и при нажатии на кнопку "ещё" будет
//отображаться 2 карточки с фильмами.
const MOBILE_CARDS_DISPLAY_LIMIT = 2
//Эта переменная определяет значение, которое использую в определении длительности
// фильмов. Тут 40минут означает, что фильмы с длительностью менее 40 минут
// будут считаться короткометражками. В функции filterDuration,
// где фильмы с длительностью меньше MAX_SHORT_FILM_LENGTH отфильтруются и вернуться как короткометражные фильмы.
const MAX_SHORT_FILM_LENGTH = 40

//Эта строка кода использует свойство innerWidth объекта window в браузере.
// Свойство innerWidth возвращает ширину области просмотра окна браузера (в пикселях)
// без учета панелей инструментов, полос прокрутки и других элементов интерфейса браузера.
// Затем значение этой ширины сохраняется в переменной display для дальнейшего использования в коде.
const display = window.innerWidth

